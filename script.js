function rotate(value) {
  document.getElementById('beer').style.webkitheight="" + value + "%";
  document.getElementById('beer').style.msheight="" + value + "%";
  document.getElementById('beer').style.Mozheight="" + value + "%";
  document.getElementById('beer').style.Oheight="" + value + "%";
  document.getElementById('beer').style.height="" + value + "%";
}